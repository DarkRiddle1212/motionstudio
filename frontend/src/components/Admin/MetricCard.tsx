import { ReactNode } from 'react';

export interface MetricChange {
  value: number;
  type: 'increase' | 'decrease';
  period: string;
}

export interface MetricCardProps {
  title: string;
  value: string | number;
  change?: MetricChange;
  icon?: ReactNode;
  iconBgColor?: string;
  iconColor?: string;
  loading?: boolean;
  onClick?: () => void;
  className?: string;
}

// Trend Arrow Icons
const TrendUpIcon = () => (
  <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 7h8m0 0v8m0-8l-8 8-4-4-6 6" />
  </svg>
);

const TrendDownIcon = () => (
  <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 17h8m0 0V9m0 8l-8-8-4 4-6-6" />
  </svg>
);

// Loading Skeleton
const MetricSkeleton = () => (
  <div className="animate-pulse">
    <div className="h-4 bg-brand-tertiary-bg rounded w-24 mb-2" />
    <div className="h-8 bg-brand-tertiary-bg rounded w-32 mb-2" />
    <div className="h-3 bg-brand-tertiary-bg rounded w-20" />
  </div>
);

const MetricCard = ({
  title,
  value,
  change,
  icon,
  iconBgColor = 'bg-brand-accent bg-opacity-20',
  iconColor = 'text-brand-accent',
  loading = false,
  onClick,
  className = '',
}: MetricCardProps) => {
  const isClickable = !!onClick;
  
  const cardClasses = `
    bg-brand-surface-bg rounded-lg shadow-card border border-brand-accent border-opacity-20 p-6
    ${isClickable ? 'cursor-pointer hover:shadow-card-hover hover:border-brand-accent hover:border-opacity-40 transition-all' : ''}
    ${className}
  `.trim();

  const content = loading ? (
    <MetricSkeleton />
  ) : (
    <div className="flex items-start justify-between">
      <div className="flex-1">
        <p className="text-sm font-medium text-brand-secondary-text mb-1">{title}</p>
        <p className="text-2xl font-bold text-brand-primary-text">{value}</p>
        {change && (
          <div className="flex items-center mt-2">
            <span
              className={`inline-flex items-center text-sm font-medium ${
                change.type === 'increase' ? 'text-brand-success' : 'text-brand-error'
              }`}
            >
              {change.type === 'increase' ? <TrendUpIcon /> : <TrendDownIcon />}
              <span className="ml-1">
                {change.type === 'increase' ? '+' : '-'}
                {Math.abs(change.value)}%
              </span>
            </span>
            <span className="ml-2 text-sm text-brand-muted-text">{change.period}</span>
          </div>
        )}
      </div>
      {icon && (
        <div className={`w-12 h-12 ${iconBgColor} rounded-lg flex items-center justify-center flex-shrink-0`}>
          <div className={iconColor}>{icon}</div>
        </div>
      )}
    </div>
  );

  if (isClickable) {
    return (
      <button onClick={onClick} className={cardClasses} type="button">
        {content}
      </button>
    );
  }

  return <div className={cardClasses}>{content}</div>;
};

